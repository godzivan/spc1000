/**
 * Initialize 6847 mode, SDL screen, and semigraphic pattern
 * @param in_VRAM pointer to VRAM array, must be preapred by caller
 */
 #define XRES 256
 #define YRES 192
 #define SCR_W 320
 #define SCR_H 240
 #define SIZE (SCR_W*SCR_H*sizeof(PIXEL))
PIXEL *InitMC6847(void)
{
	int i, j;
	rept = (SCR_H - YRES)/2 * SCR_W;
	repl = (SCR_W - XRES)/2;
	repr = (SCR_W - XRES)/2;
	repb = (SCR_H - YRES)/2 * SCR_W;
	frameBuf = (PIXEL *) malloc(SIZE);
	memset(frameBuf, 0, SIZE);
    i = 0;
	if (sizeof(PIXEL) == 1)
	{
		for(i = 0; i < 16; i++)
			cMap[i] = i;
	}
	else
	{
		cMap[i++] = Color16(0x00, 0x00, 0x00); /* BLACK */
		cMap[i++] = Color16(0x07, 0xff, 0x00); /* GREEN */ 
		cMap[i++] = Color16(0xff, 0xff, 0x00); /* YELLOW */
		cMap[i++] = Color16(0x3b, 0x08, 0xff); /* BLUE */
		cMap[i++] = Color16(0xcc, 0x00, 0x3b); /* RED */ 
		cMap[i++] = Color16(0xff, 0xff, 0xff); /* BUFF */
		cMap[i++] = Color16(0x07, 0xe3, 0x99); /* CYAN */ 
		cMap[i++] = Color16(0xff, 0x1c, 0xff); /* MAGENTA */ 
		cMap[i++] = Color16(0xff, 0x81, 0x00); /* ORANGE */

		cMap[i++] = Color16(0x07, 0xff, 0x00); /* GREEN */
		cMap[i++] = Color16(0xff, 0xff, 0xff); /* BUFF */

		cMap[i++] = Color16(0x00, 0x44, 0x00); /* ALPHANUMERIC DARK GREEN */ 
		cMap[i++] = Color16(0x07, 0xff, 0x00); /* ALPHANUMERIC BRIGHT GREEN */  
		cMap[i++] = Color16(0x91, 0x00, 0x00); /* ALPHANUMERIC DARK ORANGE */
		cMap[i++] = Color16(0xff, 0x81, 0x00);/* ALPHANUMERIC BRIGHT ORANGE */
	}
	// initialize semigraphic pattern
	for (i = 0; i < 16; i++)
		for(j = 0; j < 12; j++)
		{
			unsigned char val = 0;
			if (j < 6)
			{
				if (i & 0x08) val |= 0xf0;
				if (i & 0x04) val |= 0x0f;
			}
			else
			{
				if (i & 0x02) val |= 0xf0;
				if (i & 0x01) val |= 0x0f;
			}
			semiGrFont0[i*12+j] = val;
		}

	for (i = 0; i < 64; i++)
		for(j = 0; j < 12; j++)
		{
			unsigned char val = 0;
			if (j < 4)
			{
				if (i & 0x20) val |= 0xf0;
				if (i & 0x10) val |= 0x0f;
			}
			else if (j < 8)
			{
				if (i & 0x08) val |= 0xf0;
				if (i & 0x04) val |= 0x0f;
			}
			else
			{
				if (i & 0x02) val |= 0xf0;
				if (i & 0x01) val |= 0x0f;
			}
			semiGrFont1[i*12+j] = val;
		}
	return frameBuf;
}

